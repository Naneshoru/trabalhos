    private void menuItem_novo_brancasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItem_novo_brancasActionPerformed
        model.reset();
        
        if(!model.isWhiteBotton())labelsAdjust();        
        
        model.init();
        
        repaint();
        
        if(!getController().getUltimoLance().isEmpty()) getController().getUltimoLance().remove(0);
        getController().getUltimoLance().add(model);
        AutoSave.save(getController().getUltimoLance(), "lastMove");
    
    }//GEN-LAST:event_menuItem_novo_brancasActionPerformed

