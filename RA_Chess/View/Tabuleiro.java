    private void menuItem_inverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItem_inverterActionPerformed
        if(model.isWhiteBotton())model.setWhiteBotton(false);
        else if(!model.isWhiteBotton())model.setWhiteBotton(true);
        
        for(int i=0; i<model.getPecasBrancas().size(); i++){
            model.getPecasBrancas().get(i).setQuadrante(7 - (int)model.getPecasBrancas().get(i).getQuadrante().getX(),
                                                        7 - (int)model.getPecasBrancas().get(i).getQuadrante().getY());
        }
        for(int i=0; i<model.getPecasPretas().size(); i++){
            model.getPecasPretas().get(i).setQuadrante(7 - (int)model.getPecasPretas().get(i).getQuadrante().getX(),
                                                        7 - (int)model.getPecasPretas().get(i).getQuadrante().getY());
        }
        
        labelsAdjust();
    }//GEN-LAST:event_menuItem_inverterActionPerformed

