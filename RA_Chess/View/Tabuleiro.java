    private void menuItem_carregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItem_carregarActionPerformed
        System.out.println("carregando");
        
        ArrayList<ModelTabuleiro> lista = AutoSave.load("save");
        
        ModelTabuleiro novoModel = lista.get(lista.size()-1);
        
        model.reset();
        
        setModel(novoModel);
        canvas.getObservers().remove(canvas.getObservers().size()-1); // remove o ultimo model
        canvas.registerObserver(novoModel);
        controller.addModel(novoModel);
        
        repaint();
    }//GEN-LAST:event_menuItem_carregarActionPerformed

