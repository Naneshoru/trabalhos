    private void menuItem_voltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItem_voltarActionPerformed
        System.out.println("voltando lance");
        
        ArrayList<ModelTabuleiro> lista = AutoSave.load("lastMove");
        
        ModelTabuleiro novoModel = lista.get(lista.size()-1);
        
        model.reset();
        
        setModel(novoModel);
        canvas.getObservers().remove(canvas.getObservers().size()-1); // remove o ultimo model
        canvas.registerObserver(novoModel);
        controller.addModel(novoModel);
        
        repaint();
    }//GEN-LAST:event_menuItem_voltarActionPerformed

